import afb2
import time

try:

    afb2.gpio.reset()  # gpio 초기화 - 12초 소요
    time.sleep(4)

#===========서보모터 채널=============
#|---------------------------------|
#|          Front(Camera)          |
#|---------------------------------|
#| 11 |                       |  2 |
#|---- -----             ----- ----|
#|    | 10 |             |  1 |    |
#|    ----- -----   ----- ----     |
#|         |  9 |   |  0 |         |
#|          -----   -----          |
#|         |  6 |   |  3 |         |
#|    ----- -----   ----- ----     |
#|    |  7 |             |  4 |    |
#|---- -----             ----- ----|
#|  8 |                       |  5 |
#|---------------------------------|

#afb2.quad.servo(ch, angle) ch: 0~11채널, angle: 0~180 이나 채널 별로 간섭 여부 확인 필요

#===========서보모터 현재 각도===============
#|---------------------------------------|
#|              Front(Camera)            |
#|---------------------------------------|
#|   0 |                           | 180 |
#|----- -----                 ----- -----|
#|     | 180 |               |   0 |     |
#|      ----- -----     ----- -----      |
#            | 135 |   |  40 |           |
#|            -----     -----            |
#|           |  40 |   | 135 |           |
#|      ----- -----     ----- -----      |
#|     |   0 |               | 180 |     |
#|----- -----                 ----- -----|
#| 180 |                           |   0 |
#|---------------------------------------|

    print("Servo control started")
    print("입력 형식: 채널 각도  (예: 0 30)")
    print("종료: q")

    while True:

        cmd = input(">> ")

        if cmd == "q":
            break

        try:
            ch, angle = map(int, cmd.split())

            if 0 <= ch <= 11 and 0 <= angle <= 180:
                afb2.quad.servo(ch, angle)
                print(f"servo {ch} → {angle}")
            else:
                print("채널: 0~11, 각도: 0~180")

        except:
            print("입력 형식: 채널 각도 (예: 0 30)")

except KeyboardInterrupt:
    print("\n프로그램 종료")

finally:
    print("GPIO reset")
    afb2.gpio.reset()